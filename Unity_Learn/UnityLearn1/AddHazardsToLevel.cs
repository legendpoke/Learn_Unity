using System;
using System.Collections.Generic;
using System.Text;

namespace UnityLearn
{
    internal class AddHazardsToLevel
    {
        /*
        =================================================
        ADDING HAZARDS TO A LEVEL
        =================================================

        What are Hazards?

        Hazards are dangerous objects or areas
        that can harm, damage, or eliminate
        the player.

        Purpose:

        - Increase challenge
        - Test player skill
        - Make gameplay more exciting
        - Create risk and tension

        =================================================
        COMMON HAZARDS
        =================================================

        Examples:

        - Spikes
        - Lava
        - Fire
        - Acid
        - Electric Traps
        - Rotating Blades
        - Falling Rocks
        - Moving Obstacles
        - Explosions

        =================================================
        WHY ADD HAZARDS?
        =================================================

        Without Hazards:

        Level feels easy and boring.

        ---------------------------------

        With Hazards:

        Player must:

        - Think carefully
        - React quickly
        - Improve skills

        Gameplay becomes more engaging.

        =================================================
        HAZARD DESIGN RULE
        =================================================

        Players should be able to:

        See the hazard

                ↓

        Understand the danger

                ↓

        React to it

                ↓

        Avoid it

        Good hazards feel fair.

        =================================================
        SPIKE EXAMPLE
        =================================================

        Player touches spikes

                ↓

        Take damage

                OR

        Restart level

                OR

        Respawn at checkpoint

        =================================================
        LAVA EXAMPLE
        =================================================

        Player falls into lava

                ↓

        Health reaches zero

                ↓

        Respawn

        =================================================
        MOVING HAZARDS
        =================================================

        Examples:

        - Moving blades
        - Rolling boulders
        - Swinging hammers

        These hazards require timing
        and quick reactions.

        =================================================
        HAZARD PLACEMENT
        =================================================

        Place hazards:

        - Near jumps
        - Around obstacles
        - Between checkpoints
        - In challenge sections

        Avoid placing hazards
        randomly everywhere.

        =================================================
        DIFFICULTY PROGRESSION
        =================================================

        Early Level

                ↓

        One Hazard

                ↓

        Two Hazards

                ↓

        Multiple Hazards

                ↓

        Advanced Challenge

        Difficulty should increase gradually.

        =================================================
        COMBINING HAZARDS
        =================================================

        Example:

        Moving Platform

                +

        Spikes

                +

        Falling Rocks

        Creates a more interesting challenge.

        =================================================
        CHECKPOINTS AND HAZARDS
        =================================================

        Hazard Section

                ↓

        Checkpoint

                ↓

        Harder Hazard Section

        Checkpoints prevent frustration.

        =================================================
        RISK VS REWARD
        =================================================

        Hidden Coin

                ↓

        Near Spikes

        Player chooses:

        Take Risk

                OR

        Play Safe

        This creates interesting decisions.

        =================================================
        VISUAL DESIGN
        =================================================

        Hazards should be easy to recognize.

        Examples:

        Red Lava

        Sharp Spikes

        Bright Electricity

        Players should immediately know:

        "This is dangerous."

        =================================================
        COMMON BEGINNER MISTAKES
        =================================================

        Too Many Hazards

            ↓

        Frustrating Gameplay

        ---------------------------------

        No Checkpoints

            ↓

        Repeating Long Sections

        ---------------------------------

        Hidden Hazards

            ↓

        Feels Unfair

        =================================================
        MEMORY TRICK
        =================================================

        Hazard
            ↓
        Danger

        Spikes
            ↓
        Touch = Damage

        Lava
            ↓
        Fall = Lose

        Checkpoint
            ↓
        Progress Save

        =================================================
        SHORT REVISION
        =================================================

        Hazards
            -> Dangerous objects

        Purpose
            -> Create challenge

        Examples
            -> Spikes
            -> Lava
            -> Fire
            -> Blades

        Good Hazards
            -> Visible
            -> Fair
            -> Challenging

        Think:

        Hazards make levels exciting,
        but too many make levels frustrating.
        =================================================
        */
    }
}