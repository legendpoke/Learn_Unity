using System;
using System.Collections.Generic;
using System.Text;

namespace UnityLearn
{
    class TimeDeltaTime
    {
        /*
            Time.deltaTime

            Time taken to complete the previous frame.

            Different computers run games at different FPS.

            Example:

            PC A = 120 FPS
            PC B = 60 FPS

            Without Time.deltaTime:
            Movement would be faster on PC A.

            With Time.deltaTime:
            Movement speed stays the same on both PCs.

            This is called Frame-Rate Independent Movement.
        */
    }
}
