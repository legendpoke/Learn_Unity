        /*
        =================================================
        IMPORTING ASSETS IN UNITY
        =================================================

        What are Assets?

        Assets are files used in a Unity project.

        Examples:

        - 3D Models
        - Textures
        - Materials
        - Audio Files
        - Animations
        - Scripts
        - Prefabs
        - UI Images

        Think:

        Assets
            ↓
        Building Blocks Of A Game

        =================================================
        WHY IMPORT ASSETS?
        =================================================

        Unity projects need assets to create:

        - Characters
        - Environments
        - Obstacles
        - Sounds
        - User Interfaces

        Without assets,
        the game world would be empty.

        =================================================
        WAYS TO IMPORT ASSETS
        =================================================

        Method 1:

        Drag And Drop

        File Explorer

                ↓

        Unity Assets Folder

        =================================================
        METHOD 2
        =================================================

        Assets Menu

                ↓

        Import New Asset

                ↓

        Select File

                ↓

        Import

        =================================================
        METHOD 3
        =================================================

        Unity Asset Store

                ↓

        Download Asset

                ↓

        Import Into Project

        =================================================
        COMMON FILE TYPES
        =================================================

        3D Models

        .fbx
        .obj
        .blend

        ---------------------------------

        Images

        .png
        .jpg
        .jpeg

        ---------------------------------

        Audio

        .wav
        .mp3
        .ogg

        ---------------------------------

        Scripts

        .cs

        =================================================
        IMPORTING A 3D MODEL
        =================================================

        Example:

        Tree.fbx

                ↓

        Drag Into Assets Folder

                ↓

        Unity Imports Model

                ↓

        Drag Model Into Scene

        =================================================
        IMPORTING TEXTURES
        =================================================

        Example:

        Grass.png

                ↓

        Import Into Assets

                ↓

        Create Material

                ↓

        Apply Texture

        =================================================
        IMPORTING AUDIO
        =================================================

        Example:

        JumpSound.wav

                ↓

        Import

                ↓

        Add Audio Source

                ↓

        Play Sound

        =================================================
        PROJECT WINDOW
        =================================================

        Imported assets appear in:

        Project Window

        This is where Unity stores all
        project resources.

        =================================================
        ORGANIZING ASSETS
        =================================================

        Good Folder Structure:

        Assets

            ├── Models
            ├── Materials
            ├── Textures
            ├── Audio
            ├── Scripts
            ├── Prefabs
            └── Animations

        Organized projects are easier
        to manage.

        =================================================
        UNITY PACKAGE
        =================================================

        Some assets come as:

        .unitypackage

        Import Process:

        Double Click Package

                ↓

        Import Window Opens

                ↓

        Select Files

                ↓

        Import

        =================================================
        COMMON BEGINNER MISTAKES
        =================================================

        Importing everything into
        the root Assets folder.

        ---------------------------------

        Not organizing files.

        ---------------------------------

        Accidentally deleting imported assets.

        ---------------------------------

        Moving files outside Unity.

        =================================================
        MEMORY TRICK
        =================================================

        Asset
            ↓
        Resource Used By Game

        Import
            ↓
        Bring File Into Unity

        Project Window
            ↓
        Asset Storage Area

        =================================================
        SHORT REVISION
        =================================================

        Assets
            -> Files used in a game

        Import
            -> Add files to Unity

        Common Assets

            -> Models
            -> Textures
            -> Audio
            -> Scripts
            -> Prefabs

        Import Methods

            -> Drag & Drop
            -> Import New Asset
            -> Asset Store

        Think:

        Assets = Content

        Import = Bring Content Into Unity
        =================================================
        */
    }
